#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <stdlib.h> // malloc, free

int type_conversion_pointer()
{
	int* numPtr = malloc(sizeof(int));  // 4 byte
	char* cPtr;							// 1 byte
	
	*numPtr = 0x12345678;
	/*    12 34 56 78
	    Little Endian
		| 78 56 34 12 | <- numPtr
		  ↑
	    cPtr (only have one byte)
	*/

	cPtr = (char*)numPtr; // Type cast intpointer to charpointer.

	printf("0x%x\n", *cPtr);

	free(numPtr);

	return 0;
}

int type_conversion_pointer_invalid_value() 
{
	short* numPtr1 = malloc(sizeof(short));    // 2바이트만큼 메모리 할당
	int* numPtr2;

	*numPtr1 = 0x1234;

	numPtr2 = (int*)numPtr1;    // short 포인터 numPtr1을 int 포인터로 변환. 메모리 주소만 저장됨

	printf("0x%x\n", *numPtr2);    // 0xfdfd1234: 옆의 메모리를 침범하여 값을 가져옴
								   // 0xfdfd는 상황에 따라서 값이 달라질 수 있음

	free(numPtr1);    // 동적 메모리 해제

	return 0;
}


int type_conversion_pointer_dereference()
{
	int* numPtr = malloc(sizeof(int));    // 4바이트만큼 메모리 할당
	char* cPtr;

	*numPtr = 0x12345678;

	printf("0x%x\n", *(char*)numPtr);    // 0x78: numPtr1을 char 포인터로 변환한 뒤 역참조

	free(numPtr);    // 동적 메모리 해제

	return 0;
}
